CREATE TABLE IF NOT EXISTS `DATA_ACCESS_SUBMISSION_SUBMITTER` (
  `ID` BIGINT NOT NULL,
  `ACCESS_REQUIREMENT_ID` BIGINT NOT NULL,
  `SUBMITTER_ID` BIGINT NOT NULL,
  `CURRENT_SUBMISSION_ID` BIGINT NOT NULL,
  `ETAG` char(36) NOT NULL,
  UNIQUE KEY (`ID`),
  PRIMARY KEY (`ACCESS_REQUIREMENT_ID`, `SUBMITTER_ID`),
  CONSTRAINT `DATA_ACCESS_SUBMISSION_SUBMITTER_SUBMISSION_ID_FK` FOREIGN KEY (`CURRENT_SUBMISSION_ID`) REFERENCES `DATA_ACCESS_SUBMISSION` (`ID`) ON DELETE CASCADE,
  CONSTRAINT `DATA_ACCESS_SUBMISSION_SUBMITTER_ACCESS_REQUIREMENT_ID_FK` FOREIGN KEY (`ACCESS_REQUIREMENT_ID`) REFERENCES `ACCESS_REQUIREMENT` (`ID`) ON DELETE CASCADE
)
