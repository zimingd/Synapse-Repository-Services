CREATE TABLE IF NOT EXISTS `UPLOAD_STATUS` (
  `ID` BIGINT NOT NULL AUTO_INCREMENT,
  `STATE` ENUM('FAILED', 'COMPLETED', 'PROCESSING') NOT null,
  `STARTED_BY` BIGINT NOT NULL,
  `STARTED_ON` BIGINT NOT NULL,
  `PERCENT_COMPLETE` FLOAT NOT NULL,
  `ERROR_MESSAGE` varchar(3000) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci DEFAULT NULL,
  `FILE_HANDLE_IDS` BIGINT DEFAULT NULL,
  `RUNTIME_MS` BIGINT NOT NULL,
  PRIMARY KEY (`ID`),
  CONSTRAINT `UP_STAT_STARTED_BY_FK` FOREIGN KEY (`STARTED_BY`) REFERENCES `JDOUSERGROUP` (`ID`) ON DELETE CASCADE
)

